    private void listarPorId() throws SQLException {
        if (txtBuscarPorId.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Debe ingresar un ID", "ERROR", JOptionPane.WARNING_MESSAGE);

        }

        PersonaDAO pDAO = new PersonaDAO();
        DireccionDAO dDAO = new DireccionDAO();
        ComunaDAO cDAO = new ComunaDAO();
        ProvinciaDAO provDAO = new ProvinciaDAO();
        RegionDAO regDAO = new RegionDAO();
        DatosPersonaPostulanteDAO dppDAO = new DatosPersonaPostulanteDAO();
        DatosProyectoDAO dpDAO = new DatosProyectoDAO();

        Persona p = new Persona();
        Direccion d = new Direccion();
        Comuna c = new Comuna();
        Provincia prov = new Provincia();
        Region reg = new Region();
        DatosPersonaPostulante dpp = new DatosPersonaPostulante();
        DatosProyecto dp = new DatosProyecto();
        ResumenEje re = new ResumenEje();

        pDAO.listarPersonaPorId(Integer.parseInt(txtBuscarPorId.getText()), d);
        //dppDAO.listarDatosPersonaPostulantePorId(Integer.parseInt(txtBuscarPorId.getText()));

        try {

            ArrayList<Persona> personas = null;
            ArrayList<Direccion> direcciones = null;
            ArrayList<Comuna> comunas = null;
            ArrayList<Provincia> provincias = null;
            ArrayList<Region> regiones = null;
            ArrayList<DatosPersonaPostulante> datosPersonasPostulantes = null;
            ArrayList<DatosProyecto> datosProyectos = null;
            ArrayList<Direccion> direccionesDatosProyecto = null;
            ArrayList<Comuna> comunasDatosProyecto = null;
            ArrayList<Provincia> provinciasDatosProyecto = null;
            ArrayList<Region> regionesDatosProyecto = null;

            personas = pDAO.listarPersonaPorId(Integer.parseInt(txtBuscarPorId.getText()), d);
            direcciones = dDAO.listarDireccionPorId(d, c);
            comunas = cDAO.listarComunaPorId(prov, c);
            provincias = provDAO.listarProvinciaPorId(prov, reg);
            regiones = regDAO.listarRegionPorId(reg);
            datosPersonasPostulantes = dppDAO.listarDatosPersonaPostulantePorId(Integer.parseInt(txtBuscarPorId.getText()));
            datosProyectos = dpDAO.listarDatosProyectoPorId(d, Integer.parseInt(txtBuscarPorId.getText()));
            direccionesDatosProyecto = dDAO.listarDireccionDatosProyPorId(d, c);
            comunasDatosProyecto = cDAO.listarComunaDatosProyectoPorId(prov, c);
            provinciasDatosProyecto = provDAO.listarProvinciaDatosProyectoPorId(prov, reg);
            regionesDatosProyecto = regDAO.listarRegionDatosProyectoPorId(reg);

            if (personas.size() == 0) {

                JOptionPane.showMessageDialog(null, "No se encontro la persona con el ID ingresado", "ERROR", JOptionPane.WARNING_MESSAGE);

            }

            DefaultTableModel modeloPer = new DefaultTableModel();

            modeloPer.addColumn("Nombre");
            modeloPer.addColumn("Apellido paterno");
            modeloPer.addColumn("Apellido materno");
            modeloPer.addColumn("Rut");
            modeloPer.addColumn("Telefono");
            modeloPer.addColumn("Telefono 2");
            modeloPer.addColumn("Correo");

            DefaultTableModel modeloPer2 = new DefaultTableModel();

            modeloPer2.addColumn("Pueblo indigena perteneciente");
            modeloPer2.addColumn("Fecha postulacion");
            modeloPer2.addColumn("NÂ° certificado indigena");

            DefaultTableModel modeloDireccionPer = new DefaultTableModel();

            modeloDireccionPer.addColumn("Direccion");
            modeloDireccionPer.addColumn("Comuna");
            modeloDireccionPer.addColumn("Provincia");
            modeloDireccionPer.addColumn("Region");
            modeloDireccionPer.addColumn("Sector");

            DefaultTableModel modeloDatosProyecto = new DefaultTableModel();

            modeloDatosProyecto.addColumn("Nombre del proyecto");
            modeloDatosProyecto.addColumn("Nombre propietario inmueble");
            modeloDatosProyecto.addColumn("Situacion en que propietario facilita postulante");

            DefaultTableModel modeloDireccionDatosProyecto = new DefaultTableModel();

            modeloDireccionDatosProyecto.addColumn("Direccion proyecto");
            modeloDireccionDatosProyecto.addColumn("Sector/Comunidad");
            modeloDireccionDatosProyecto.addColumn("Comuna");
            modeloDireccionDatosProyecto.addColumn("Provincia");
            modeloDireccionDatosProyecto.addColumn("Region");

            for (Persona per : personas) {
                String[] fila = new String[7];
                fila[0] = per.getNombres();
                fila[1] = per.getApePat();
                fila[2] = per.getApeMat();
                fila[3] = per.getRut();
                fila[4] = Integer.toString(per.getTelefono());
                fila[6] = per.getCorreo();

                for (DatosPersonaPostulante datpp : datosPersonasPostulantes) {
                    fila[5] = datpp.getTelefonoDos();

                    modeloPer.addRow(fila);

                }

            }

            tblDatosPersona.setModel(modeloPer);

            for (Persona per2 : personas) {
                String[] fila = new String[3];
                fila[0] = per2.getPuebloIndigena();
                fila[1] = per2.getFechaPostulacion();

                for (DatosPersonaPostulante datpp : datosPersonasPostulantes) {
                    fila[2] = Integer.toString(datpp.getNumCertIndigena());

                    modeloPer2.addRow(fila);
                }

            }

            tblDatosPersonaDos.setModel(modeloPer2);

            for (Direccion dir : direcciones) {
                String[] fila = new String[5];
                fila[0] = dir.getDireccion();

                for (Comuna com : comunas) {
                    fila[1] = com.getNombreCom();

                    for (Provincia provi : provincias) {
                        fila[2] = provi.getNombreProv();

                        for (Region regi : regiones) {
                            fila[3] = regi.getNombreReg();

                            for (DatosPersonaPostulante datpp : datosPersonasPostulantes) {
                                fila[4] = datpp.getSector();

                                modeloDireccionPer.addRow(fila);
                            }

                        }
                    }
                }
            }

            tblDireccionPersona.setModel(modeloDireccionPer);

            for (DatosProyecto datosProy : datosProyectos) {
                String[] fila = new String[3];
                fila[0] = datosProy.getNombreProy();
                fila[1] = datosProy.getPropImplProy();
                fila[2] = datosProy.getSitPropInm();

                modeloDatosProyecto.addRow(fila);

            }

            tblDatosProyecto.setModel(modeloDatosProyecto);

            for (Direccion dirDatosProy : direccionesDatosProyecto) {
                String[] fila = new String[5];

                fila[0] = dirDatosProy.getDireccionDatosProyecto();

                for (DatosProyecto datosP : datosProyectos) {
                    fila[1] = datosP.getSectorComunidad();

                    for (Comuna dirCom : comunasDatosProyecto) {
                        fila[2] = dirCom.getNombreComDatosProyecto();
                        
                        for(Provincia dirProv : provinciasDatosProyecto){
                            fila[3] = dirProv.getNombreProvDatosProyecto();
                            
                            for(Region dirReg : regionesDatosProyecto){
                                fila[4] = dirReg.getNombreRegDatosProyecto();
                                
                                modeloDireccionDatosProyecto.addRow(fila);
                                
                            }
                        }
                    }
                }

            }
            
            
            tblDireccionProyecto.setModel(modeloDireccionDatosProyecto);
            

        } catch (Exception e) {
            System.out.println("ERROR: " + e.getMessage());
        }

    }

